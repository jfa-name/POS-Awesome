<script>
import { evntBus } from '../../bus';
import SignatureInput from './SignatureInput.vue';

export default {
  components: {
    SignatureInput,
  },
  data: () => ({
    loading: false,
    pos_profile: '',
    deliverynote_doc: '',
    loyalty_amount: 0,
    is_credit_sale: 0,
    is_write_off_change: 0,
    date_menu: false,
    po_date_menu: false,
    addresses: [],
    sales_persons: [],
    sales_person: '',
    paid_change: 0,
    order_delivery_date: false,
    paid_change_rules: [],
    is_return: false,
    is_cashback: true,
    redeem_customer_credit: false,
    customer_credit_dict: [],
    phone_dialog: false,
    deliverynoteType: 'Delivery Note',
    pos_settings: '',
    customer_info: '',
    mpesa_modes: [],
    float_precision: 2,
    currency_precision: 2,
    signatureData: '',
  }),

  methods: {
    openSignatureDialog() {
      this.$refs.signatureInput.openSignatureDialog();
    },
    saveSignature(signatureDataURL) {
      this.deliverynote_doc.signature = signatureDataURL;
    },
    validateDeliveryNote() {
      if (!this.deliverynote_doc.signature) {
        alert('Please provide a signature before validating the Delivery Note.');
        return false;
      }
      return true;
    },
    clearSignature() {
      this.deliverynote_doc.signature = ''; // Clear the signature data
      this.$refs.signatureInput.clearSignature();
    },
    back_to_deliverynote() {
      evntBus.$emit('show_proceed', 'false');
      evntBus.$emit('set_customer_readonly', false);
    },
    submit(event, payment_received = false, print = false) {
      // Call the validation method before submitting
      if (!this.validateDeliveryNote()) {
        return;
      }
      this.submit_deliverynote(print);
      this.customer_credit_dict = [];
      this.redeem_customer_credit = false;
      this.is_cashback = true;
      this.sales_person = '';
      evntBus.$emit('new_deliverynote', 'false');
      this.back_to_deliverynote();
    },
    submit_deliverynote(print) {
      let data = {
        deliverynote: this.deliverynote_doc, // Pass the deliverynote_doc object directly
        total_change: -this.diff_payment,
        paid_change: this.paid_change,
        credit_change: -this.credit_change,
        redeemed_customer_credit: this.redeemed_customer_credit,
        customer_credit_dict: this.customer_credit_dict,
        is_cashback: this.is_cashback,
      };

      const vm = this;
      frappe.call({
        method: 'posawesome.posawesome.api.posapp.submit_deliverynote',
        args: {
          data: data,
        },
        async: true,
        callback: function (r) {
          if (r.message) {
            if (print) {
              vm.load_print_page(r.message.name);
            }
            evntBus.$emit('set_last_deliverynote', vm.deliverynote_doc.name);
            evntBus.$emit('show_message', {
              text: `Delivery Note ${r.message.name} is Submitted`,
              color: 'success',
            });
            frappe.utils.play_sound('submit');
            this.addresses = [];
          }
        },
      });
    },

    set_full_amount(idx) {
      this.deliverynote_doc.payments.forEach((payment) => {
        payment.amount = payment.idx == idx ? this.deliverynote_doc.grand_total : 0;
      });
    },
    set_rest_amount(idx) {
      this.deliverynote_doc.payments.forEach((payment) => {
        if (
          payment.idx == idx &&
          payment.amount == 0 &&
          this.diff_payment > 0
        ) {
          payment.amount = this.diff_payment;
        }
      });
    },
    clear_all_amounts() {
      this.deliverynote_doc.payments.forEach((payment) => {
        payment.amount = 0;
      });
    },
    async load_print_page() {
      await this.validateDeliveryNote();
      const print_format =
        this.pos_profile.print_format_for_online_dn ||
        this.pos_profile.print_format_dn;
      const letter_head = this.pos_profile.letter_head || 0;
      const url =
        frappe.urllib.get_base_url() +
        '/printview?doctype=Delivery%20Note&name=' +
        this.deliverynote_doc.name +
        '&trigger_print=1' +
        '&format=' +
        print_format +
        '&no_letterhead=' +
        letter_head;
      const printWindow = window.open(url, 'Print');
      printWindow.addEventListener(
        'load',
        function () {
          printWindow.print();
          // printWindow.close();
          // NOTE : uncomoent this to auto closing printing window
        },
        true
      );
    },
    validate_due_date() {
      const today = frappe.datetime.now_date();
      const parse_today = Date.parse(today);
      const new_date = Date.parse(this.deliverynote_doc.due_date);
      if (new_date < parse_today) {
        setTimeout(() => {
          this.deliverynote_doc.due_date = today;
        }, 0);
      }
    },
    formtCurrency(value) {
      value = parseFloat(value);
      return value
        .toFixed(this.currency_precision)
        .replace(/\d(?=(\d{3})+\.)/g, '$&,');
    },
    formtFloat(value) {
      value = parseFloat(value);
      return value
        .toFixed(this.float_precision)
        .replace(/\d(?=(\d{3})+\.)/g, '$&,');
    },
    shortPay(e) {
      if (e.key === 'x' && (e.ctrlKey || e.metaKey)) {
        e.preventDefault();
        this.submit();
      }
    },
    set_paid_change() {
      if (!this.paid_change) this.paid_change = 0;

      this.paid_change_rules = [];
      let change = -this.diff_payment;
      if (this.paid_change > change) {
        this.paid_change_rules = [
          'Paid change can not be greater than total change!',
        ];
        this.credit_change = 0;
      }
    },
    get_available_credit(e) {
      this.clear_all_amounts();
      if (e) {
        frappe
          .call('posawesome.posawesome.api.posapp.get_available_credit', {
            customer: this.deliverynote_doc.customer,
            company: this.pos_profile.company,
          })
          .then((r) => {
            const data = r.message;
            if (data.length) {
              this.customer_credit_dict = data;
            } else {
              this.customer_credit_dict = [];
            }
          });
      } else {
        this.customer_credit_dict = [];
      }
    },
    get_addresses() {
      const vm = this;
      if (!vm.deliverynote_doc) {
        return;
      }
      frappe.call({
        method: 'posawesome.posawesome.api.posapp.get_customer_addresses',
        args: { customer: vm.deliverynote_doc.customer },
        async: true,
        callback: function (r) {
          if (!r.exc) {
            vm.addresses = r.message;
          } else {
            vm.addresses = [];
          }
        },
      });
    },
    addressFilter(item, queryText, itemText) {
      const textOne = item.address_title
        ? item.address_title.toLowerCase()
        : '';
      const textTwo = item.address_line1
        ? item.address_line1.toLowerCase()
        : '';
      const textThree = item.address_line2
        ? item.address_line2.toLowerCase()
        : '';
      const textFour = item.city ? item.city.toLowerCase() : '';
      const textFifth = item.name.toLowerCase();
      const searchText = queryText.toLowerCase();
      return (
        textOne.indexOf(searchText) > -1 ||
        textTwo.indexOf(searchText) > -1 ||
        textThree.indexOf(searchText) > -1 ||
        textFour.indexOf(searchText) > -1 ||
        textFifth.indexOf(searchText) > -1
      );
    },
    new_address() {
      evntBus.$emit('open_new_address', this.deliverynote_doc.customer);
    },
    get_sales_person_names() {
      const vm = this;
      if (
        vm.pos_profile.posa_local_storage &&
        localStorage.sales_persons_storage
      ) {
        vm.sales_persons = JSON.parse(
          localStorage.getItem('sales_persons_storage')
        );
      }
      frappe.call({
        method: 'posawesome.posawesome.api.posapp.get_sales_person_names',
        callback: function (r) {
          if (r.message) {
            vm.sales_persons = r.message;
            if (vm.pos_profile.posa_local_storage) {
              localStorage.setItem('sales_persons_storage', '');
              localStorage.setItem(
                'sales_persons_storage',
                JSON.stringify(r.message)
              );
            }
          }
        },
      });
    },
    salesPersonFilter(item, queryText, itemText) {
      const textOne = item.sales_person_name
        ? item.sales_person_name.toLowerCase()
        : '';
      const textTwo = item.name.toLowerCase();
      const searchText = queryText.toLowerCase();

      return (
        textOne.indexOf(searchText) > -1 || textTwo.indexOf(searchText) > -1
      );
    },
    request_payment() {
      this.phone_dialog = false;
      const vm = this;
      if (!this.deliverynote_doc.contact_mobile) {
        evntBus.$emit('show_mesage', {
          text: __(`Pleas Set Customer Mobile Number`),
          color: 'error',
        });
        evntBus.$emit('open_edit_customer');
        this.back_to_deliverynote();
        return;
      }
      evntBus.$emit('freeze', {
        title: __(`Waiting for payment... `),
      });

      let formData = { ...this.deliverynote_doc };
      formData['total_change'] = -this.diff_payment;
      formData['paid_change'] = this.paid_change;
      formData['credit_change'] = -this.credit_change;
      formData['redeemed_customer_credit'] = this.redeemed_customer_credit;
      formData['customer_credit_dict'] = this.customer_credit_dict;
      formData['is_cashback'] = this.is_cashback;

      frappe
        .call({
          method: 'posawesome.posawesome.api.posapp.update_deliverynote',
          args: {
            data: formData,
          },
          async: false,
          callback: function (r) {
            if (r.message) {
              vm.deliverynote_doc = r.message;
            }
          },
        })
        .then(() => {
          frappe
            .call({
              method: 'posawesome.posawesome.api.posapp.create_payment_request',
              args: {
                doc: vm.deliverynote_doc,
              },
            })
            .fail(() => {
              evntBus.$emit('unfreeze');
              evntBus.$emit('show_mesage', {
                text: __(`Payment request failed`),
                color: 'error',
              });
            })
            .then(({ message }) => {
              const payment_request_name = message.name;
              setTimeout(() => {
                frappe.db
                  .get_value('Payment Request', payment_request_name, [
                    'status',
                    'grand_total',
                  ])
                  .then(({ message }) => {
                    if (message.status != 'Paid') {
                      evntBus.$emit('unfreeze');
                      evntBus.$emit('show_mesage', {
                        text: __(
                          `Payment Request took too long to respond. Please try requesting for payment again`
                        ),
                        color: 'error',
                      });
                    } else {
                      evntBus.$emit('unfreeze');
                      evntBus.$emit('show_mesage', {
                        text: __('Payment of {0} received successfully.', [
                          vm.formtCurrency(
                            message.grand_total,
                            vm.deliverynote_doc.currency,
                            0
                          ),
                        ]),
                        color: 'success',
                      });
                      frappe.db
                        .get_doc('Delivery Note', vm.deliverynote_doc.name)
                        .then((doc) => {
                          vm.deliverynote_doc = doc;
                          vm.submit(null, true);
                        });
                    }
                  });
              }, 30000);
            });
        });
    },
    get_mpesa_modes() {
      const vm = this;
      frappe.call({
        method: 'posawesome.posawesome.api.m_pesa.get_mpesa_mode_of_payment',
        args: { company: vm.pos_profile.company },
        async: true,
        callback: function (r) {
          if (!r.exc) {
            vm.mpesa_modes = r.message;
          } else {
            vm.mpesa_modes = [];
          }
        },
      });
    },
    is_mpesa_c2b_payment(payment) {
      if (
        this.mpesa_modes.includes(payment.mode_of_payment) &&
        payment.type == 'Bank'
      ) {
        payment.amount = 0;
        return true;
      } else {
        return false;
      }
    },
    mpesa_c2b_dialg(payment) {
      const data = {
        company: this.pos_profile.company,
        mode_of_payment: payment.mode_of_payment,
        customer: this.deliverynote_doc.customer,
      };
      evntBus.$emit('open_mpesa_payments', data);
    },
    set_mpesa_payment(payment) {
      this.pos_profile.use_customer_credit = 1;
      this.redeem_customer_credit = true;
      const advance = {
        type: 'Advance',
        credit_origin: payment.name,
        total_credit: payment.unallocated_amount,
        credit_to_redeem: payment.unallocated_amount,
      };
      this.clear_all_amounts();
      this.customer_credit_dict.push(advance);
    },
  },

  computed: {
    total_payments() {
      let total = parseFloat(this.deliverynote_doc.loyalty_amount);
      if (this.deliverynote_doc && this.deliverynote_doc.payments) {
        this.deliverynote_doc.payments.forEach((payment) => {
          total += parseFloat(payment.amount);
        });
      }

      total += parseFloat(this.redeemed_customer_credit);

      if (!this.is_cashback) total = 0;

      return total.toFixed(this.currency_precision);
    },
    diff_payment() {
      let diff_payment = (
        this.deliverynote_doc.grand_total - this.total_payments
      ).toFixed(this.currency_precision);
      this.paid_change = -diff_payment;
      return diff_payment;
    },
    credit_change() {
      let change = -this.diff_payment;
      if (this.paid_change > change) return 0;
      return (this.paid_change - change).toFixed(this.currency_precision);
    },
    diff_lable() {
      let lable = this.diff_payment < 0 ? 'Change' : 'To Be Paid';
      return lable;
    },
    available_pioints_amount() {
      let amount = 0;
      if (this.customer_info.loyalty_points) {
        amount =
          this.customer_info.loyalty_points *
          this.customer_info.conversion_factor;
      }
      return amount;
    },
    available_customer_credit() {
      let total = 0;
      this.customer_credit_dict.map((row) => {
        total += row.total_credit;
      });

      return total;
    },
    redeemed_customer_credit() {
      let total = 0;
      this.customer_credit_dict.map((row) => {
        if (row.credit_to_redeem) total += parseFloat(row.credit_to_redeem);
        else row.credit_to_redeem = 0;
      });

      return total;
    },
    vaildatPayment() {
      if (this.pos_profile.posa_allow_sales_order) {
        if (
          this.deliverynoteType == 'Order' &&
          !this.deliverynote_doc.posa_delivery_date
        ) {
          return true;
        } else {
          return false;
        }
      } else {
        return false;
      }
    },
    request_payment_field() {
      let res = false;
      if (!this.pos_settings || this.pos_settings.deliverynote_fields.length == 0) {
        res = false;
      } else {
        this.pos_settings.deliverynote_fields.forEach((el) => {
          if (
            el.fieldtype == 'Button' &&
            el.fieldname == 'request_for_payment'
          ) {
            res = true;
          }
        });
      }
      return res;
    },
  },

  created: function () {
    this.$nextTick(function () {
      evntBus.$on('send_deliverynote_doc_payment', (deliverynote_doc) => {
        this.deliverynote_doc = deliverynote_doc;
        this.deliverynote_doc.payments = this.deliverynote_doc.payments || [];
        const default_payment = this.deliverynote_doc.payments.find(
          (payment) => payment.default == 1
        );
        this.is_credit_sale = 0;
        this.is_write_off_change = 0;
        if (default_payment) {
          default_payment.amount = deliverynote_doc.grand_total.toFixed(
            this.currency_precision
          );
        }
        this.loyalty_amount = 0;
        this.get_addresses();
        this.get_sales_person_names();
      });
      evntBus.$on('register_pos_profile', (data) => {
        this.pos_profile = data.pos_profile;
        this.get_mpesa_modes();
        this.float_precision =
          frappe.defaults.get_default('float_precision') || 2;
        this.currency_precision =
          frappe.defaults.get_default('currency_precision') || 2;
      });
      evntBus.$on('add_the_new_address', (data) => {
        this.addresses.push(data);
        this.$forceUpdate();
      });
      evntBus.$on('update_deliverynote_type', (data) => {
        this.deliverynoteType = data;
        if (this.deliverynote_doc && data != 'Order') {
          this.deliverynote_doc.posa_delivery_date = null;
          this.deliverynote_doc.posa_notes = null;
          this.deliverynote_doc.shipping_address_name = null;
        }
      });
    });
    evntBus.$on('update_customer', (customer) => {
      if (this.customer != customer) {
        this.customer_credit_dict = [];
        this.redeem_customer_credit = false;
        this.is_cashback = true;
      }
    });
    evntBus.$on('set_pos_settings', (data) => {
      this.pos_settings = data;
    });
    evntBus.$on('set_customer_info_to_edit', (data) => {
      this.customer_info = data;
    });
    evntBus.$on('set_mpesa_payment', (data) => {
      this.set_mpesa_payment(data);
    });
    document.addEventListener('keydown', this.shortPay.bind(this));
  },

  destroyed() {
    document.removeEventListener('keydown', this.shortPay);
  },

  watch: {
    loyalty_amount(value) {
      if (value > this.available_pioints_amount) {
        this.deliverynote_doc.loyalty_amount = 0;
        this.deliverynote_doc.redeem_loyalty_points = 0;
        this.deliverynote_doc.loyalty_points = 0;
        evntBus.$emit('show_mesage', {
          text: `Loyalty Amount can not be more then ${this.available_pioints_amount}`,
          color: 'error',
        });
      } else {
        this.deliverynote_doc.loyalty_amount = flt(this.loyalty_amount);
        this.deliverynote_doc.redeem_loyalty_points = 1;
        this.deliverynote_doc.loyalty_points =
          flt(this.loyalty_amount) / this.customer_info.conversion_factor;
      }
    },
    is_credit_sale(value) {
      if (value == 1) {
        this.deliverynote_doc.payments.forEach((payment) => {
          payment.amount = 0;
          payment.base_amount = 0;
        });
      }
    },
    is_write_off_change(value) {
      if (value == 1) {
        this.deliverynote_doc.write_off_amount = this.diff_payment;
        this.deliverynote_doc.write_off_outstanding_amount_automatically = 1;
      } else {
        this.deliverynote_doc.write_off_amount = 0;
        this.deliverynote_doc.write_off_outstanding_amount_automatically = 0;
      }
    },
    redeemed_customer_credit(value) {
      if (value > this.available_customer_credit) {
        evntBus.$emit('show_mesage', {
          text: `You can redeem customer credit upto ${this.available_customer_credit}`,
          color: 'error',
        });
      }
    },
    sales_person() {
      if (this.sales_person) {
        this.deliverynote_doc.sales_team = [
          {
            sales_person: this.sales_person,
            allocated_percentage: 100,
          },
        ];
      } else {
        this.deliverynote_doc.sales_team = [];
      }
    },
  },
};
</script>
